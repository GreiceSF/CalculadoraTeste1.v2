package CalcJunt;

public class Servicecalc {

    public double somar;
    public double subtrair;
    public double multiplicar;
    public double dividir;

    public double favorsomar (double num1, double num2){
        somar = num1 + num2;
        return somar;
    }

    public double favorsubtrair (double num1, double num2){
        subtrair = num1 - num2;
        return  subtrair;
    }

    public double favormultiplicar (double num1, double num2){
        multiplicar = num1 * num2;
        return multiplicar;
    }

    public double favordividir (double num1, double num2){
        dividir = num1 / num2;
        return dividir;
    }
}










